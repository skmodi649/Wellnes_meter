package com.example.wellnesmeter;

public class HealthSample {
    private String Day;
    private int Temp;
    private int Pulse;
    private int Diast;
    private int Syst;

    public String getDay() {
        return Day;
    }

    public void setDay(String day) {
        Day = day;
    }

    public int getTemp() {
        return Temp;
    }

    public void setTemp(int temp) {
        Temp = temp;
    }

    public int getPulse() {
        return Pulse;
    }

    public void setPulse(int pulse) {
        Pulse = pulse;
    }

    public int getDiast() {
        return Diast;
    }

    public void setDiast(int diast) {
        Diast = diast;
    }

    public int getSyst() {
        return Syst;
    }

    public void setSyst(int syst) {
        Syst = syst;
    }
}
